(section .rom0)

; Do not turn the LCD off outside of VBlank
(label 'wait-vb-blank) ;0x155
(ld %a ('hw-ly))
(cp 144)
(jr #c 'wait-vb-blank)

; Turn the LCD off
(ld %a 0)
(ld ('hw-lcdc) %a)

(ld %de 'tiles)
(ld %hl 0x9000)
(ld %bc (- 'tiles-end 'tiles))
(call 'mem-copy)

; Copy the tilemap
(ld %de 'tilemap)
(ld %hl 0x9800)
(ld %bc (- 'tilemap-end 'tilemap))
(call 'mem-copy)


; Turn the LCD on
(ld %a (| +lcdc-on+ +lcdc-bg-on+))
(ld ('hw-lcdc) %a)

; During the first (blank) frame, initialize display registers
(ld %a 0b11100100)
(ld ('hw-bgp) %a)

('done jr 'done)

(label 'mem-copy)
(ld %a (%de))
(ld (%hl +) %a)
(inc %de)
(dec %bc)
(ld %a %b)
(or %a %c)
(jr #nz 'mem-copy)
(ret)

