(section .rom0)
; Shut down audio circuitry
(ld %a 0) ;0x150
(ld ('hw-sound) %a) ;0x152

; Do not turn the LCD off outside of VBlank
(label 'wait-vb-blank) ;0x155
(ld %a ('hw-ly))
(cp 144)
(jp #c 'wait-vb-blank)

; Turn the LCD off
(ld %a 0)
(ld ('hw-lcdc) %a)

; Copy the tile data
(ld %de 'tiles)
(ld %hl 0x9000)
(ld %bc (- 'tiles-end 'tiles))

(label 'copy-tiles)
(ld %a (%de))
(ld (%hl +) %a)
(inc %de)
(dec %bc)
(ld %a %b)
(or %a %c)
(jp #nz 'copy-tiles)

; Copy the tilemap
(ld %de 'tilemap)
(ld %hl 0x9800)
(ld %bc (- 'tilemap-end 'tilemap))

(label 'copy-tilemap)
(ld %a (%de))
(ld (%hl +) %a)
(inc %de)
(dec %bc)
(ld %a %b)
(or %a %c)
(jr #nz 'copy-tilemap)

; Turn the LCD on
;(ld %a (| +lcdc-on+ +lcdc_bg_on+))
(ld ('hw-lcdc) %a)

; During the first (blank) frame, initialize display registers
(ld %a 0b11100100)
(ld ('hw-bgp) %a)

('done jr 'done)

